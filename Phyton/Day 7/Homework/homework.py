num1 = int(input("Number 1: ")) # შემოვატანინოთ პირველი რიცხვი
num2 = int(input("Number 2: ")) # შემოვატანინოთ მეორე რიცხვი
num3 = int(input("Number 3: ")) # შემოვატანინოთ მესამე რიცხვი
num4 = int(input("Number 4: ")) # შემოვატანინოთ მეოთხე რიცხვი
num5 = int(input("Number 5: ")) # შემოვატანინოთ მეხუთე რიცხვი

print(num1 + num2) # შეკრება
print(num3 - num4) # გამოკლება
print(num5 * num1) # გამრავლება
print(num2 / num3) # ზუსტი გაყოფა
print(num4 // num5) # გავიგებთ თუ რამდენჯერ მოთავსდება გამყოფი გასაყოფში
print(num1 % num2) # გავიგებთ ნაშთს